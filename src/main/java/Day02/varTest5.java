package Day02;

/*

	java常量的默认类型

	1.java的整型常量默认为 int 型
	2.Java 的浮点型常量默认为double型
*/
public class varTest5 {

    public static void main(String[] args){

        double d = 12.3; //2.Java 的浮点型常量默认为double型

        float f = 12.5f;

        System.out.println(f);


        System.out.println("-------------------------------------");

        long number = 1234564564656L; //java的整型常量默认为 int 型
        System.out.println(number);


        byte by = 12;
        System.out.println(by);

    }
}
